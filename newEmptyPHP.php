        <h2>Instructions</h2>
        <h3>Settings - General Settings</h3>
        <div>
            <p>
                The settings section is found on the Wordpress menu: Settings -> DD Contact Form and contains three sections: General Settings, User Interface and Additional Information.
            <h3>General Settings</h3>
            Set, edit or view email settings. View or change captcha settings in security settings section. Enable user details logging in privacy settings section.
            <strong>Security</strong>
            Select captcha type: simple addition (default) or google reCaptcha. To use the Google reCaptcha, you will need to sign up and get private and public keys. Signup is extremely quick and easy - see <a href="https://www.google.com/recaptcha/admin/create">here</a>
            <strong>Privacy</strong>
            Offer to save user permission to send newsletters or other updates in the future.
            Save the IP address and geolocation information for each contact form user. Please ensure there are no legal issues using this in your area before enabling.
            To use the geolocation services, you will need to register with IPinfoDB to get a key. Registration is relatively straightforward - see <a href="http://www.ipinfodb.com/register.php">here</a> 
            <strong>Email</strong>
            Set recipient email addresses for receiving contact form enquiries.
            Configure the sending of a confirmation email to the user once the system has received the form.
            Specify an image to display at the top of emails sent by the contact form. The URL should be in absolute format. 
            </p>
