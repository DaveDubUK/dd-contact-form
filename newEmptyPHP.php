    <h2>About DD Contact Form</h2>
    Whilst working on WordPress websites for friends, I found I was unable to find a free contact form plugin that had all the features I needed. The ones that came with themes I'd bought were not so good either, so I started making my own. After 18 months evolution, the plugin was ready. I decided to release it open source, as I feel I've benefited greatly from the community over the years. I hope you find it useful. Please report any bugs <a href="https://groups.google.com/forum/#!forum/dd-contact-form">here</a>.
    <br>
    <h2>Features</h2>
    <ul>
        <li>easy to use - works straight out of the box, just add shortcode</li>
        <li>easy to set up - no registration</li>
